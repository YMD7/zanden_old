    <h3>Philosophy</h3>
    
    <h4>I</h4>
    <p>
    Audio – if viewed from the perspective of being the mechanical extension of musical records – could also be regarded as a logical development from the sound-box type recorder of the SP era. However, it is more than simply a mechanical tool or device for the reproduction of an electrical signal. It must recreate, with high fidelity, the charge and emotional content of live music. Consequently, the audio amplifier plays a pivotal role in expressing sound.</p>
    <p>
    However good the speakers are, we would never be able to extract the glorious, wonderful sounds captured for posterity by studio recorders, if the amplifier were imperfect – a fact that has been proven over and over again. In this regard, the amplifier could rightfully be viewed as the heart of the audio system. From early on, I came to realize the importance of this and decided to dedicate myself to a higher goal – of becoming an expert in amplifier design.</p>
    
    <h4>II</h4>
    <p>
    I decided to major in electrical and electronic engineering at university to fulfill my desire of fully comprehending even the finest nuance of audio amplifier operations. To date, I have devoted more than 30 years of time and effort to researching this. During this time, I have been involved with not just technology, but also classical and jazz live concerts as an "audio coordinator."</p>
    <p>
    From my involvement in over 500 live performances, I have experienced first-hand how the gap between the live performance and its reproduction might appear to be small but is actually very wide.
I have felt this strange feeling of disconnection that arises from this gap. And, I have also seen it shared by many other listeners who love and enjoy music. This prompted me to realize that, as an audio engineer, I had a sacred mission to bridge this gap.</p>
    <p>
    In order for me to accomplish this goal, I had to first understand the reasons why this gap exists. As a result, I spent a lot of time researching this. Finally, I discovered correlations between the realistic balance of music and the phase components embedded in the audio signal.</p>
    <p>
    Real-life examples include the inconsistencies in the recording equalization curve (represented by RIAA) for analogue stereo LPs and the influence of digital low-pass filters of causing phase distortion in rising frequencies. In short, we can realize real audio – which aims to reproduce music with utmost fidelity – only by fully accounting for these correlations.</p>
    
    <h4>III</h4>
    <p>The product that grew out of this conviction and research was the Zanden brand of fine audio components. During its development and commercialization, I reexamined traditional vacuum tube amplification technology so that Zanden audio components could incorporate the most suitable, state-of-the-art devices. The end result is our very own, unique amplifier technology.</p>
    <p>
    I recollect an old saying that teaches us that we can discover new knowledge by revisiting old books. Similarly, if we ignore the past and rely solely on the latest technologies, it will be impossible to fully comprehend and appreciate the traditional arts of music. This enlightened understanding is proven by the performance of our revolutionary, though seemingly antiquated, D/A converter which has been touted as the pinnacle of digital audio design. This particular technology is poised to play a major role in digital amplifiers in the near future.</p>
